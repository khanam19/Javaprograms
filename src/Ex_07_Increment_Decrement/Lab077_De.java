package Ex_07_Increment_Decrement;

public class Lab077_De
{
    public static void main(String[] args) {
        int a = 10;
        int result_post = a--;
        System.out.println(result_post); // 10
        System.out.println(a); //9

        int a1= 10;
        int result = --a1; // 9
        System.out.println(result);
        System.out.println(a1); // 9

    }
}

